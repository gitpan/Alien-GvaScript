use strict;
use warnings;

use HTTP::Daemon;
use HTTP::Status;
use URI::Escape;

my @dico = <DATA>;
chomp foreach @dico;

my $port = $ARGV[0] || 8080;

my $d = HTTP::Daemon->new(LocalPort => $port) || die;
print "Please contact me at: <URL:", $d->url, ">\n";
while (my $c = $d->accept) {
  while (my $req = $c->get_request) {
    my $path_info = substr(uri_unescape($req->url->path), 1);
    print STDERR "REQUEST: $path_info\n";

    if ($path_info =~ s[^word/][]) {
      my @words = grep /^$path_info/, @dico;
      my $json = join ",", map {qq{"$_"}} @words;
      
      my $response = HTTP::Response->new;
      $response->header(
        'Content-Type'  => 'text/javascript; charset=ISO-8859-1',
        'Cache-Control' => 'no-cache, must-revalidate, max-age=0',
        'Pragma'        => 'no-cache',
        'Expires'       => '0');
      $response->content("[$json]");
      $c->send_response($response);
    }
    else {
      $c->send_file_response("../../$path_info");
    } 
  }
  $c->close;
  undef($c);
}

